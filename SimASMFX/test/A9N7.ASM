	LOD A, [COUNT]
LOOP1:	SUB A, [ONE]
	CALL FIB
	JNZ LOOP1
	HLT
COUNT:	DB  0A
FIRST:	DB  00
LAST:	DB  01
SUM:	DB  00
ONE:	DB  01

FIB:	PUSH A
	PUSH SR
	LOD A, [FIRST]
	ADD A, [LAST]
	STO A, [SUM]
	LOD A, [LAST]
	STO A, [FIRST]
	LOD A, [SUM]
	STO A, [LAST]
	LOD A, [SUM]
	OUT 00, A
	POP SR
	POP A
	RET

